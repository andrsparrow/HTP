package by.pht.test2_solar_system;

public class Moon {
	private String name;
	private int size;
	
	
	
	
	public Moon(String name, int size) {
		super();
		this.name = name;
		this.size = size;
	}




	@Override
	public String toString() {
		return "Moon name=" + name + ", size=" + size + "";
	}
	
	
	

	
	
}
